// File watcher — tracks watched directories per window
const watchers = new Map();

ipcMain.handle('fs:watch', (e, dirPath) => {
  const watchId = `${e.sender.id}:${dirPath}`;
  if (watchers.has(watchId)) return watchId;

  try {
    const watcher = fs.watch(dirPath, { recursive: true }, (eventType, filename) => {
      if (!filename) return;
      const fullPath = normalizeFsPath(path.join(dirPath, filename));
      const kind = eventType === 'rename'
        ? (fs.existsSync(path.join(dirPath, filename)) ? 'created' : 'deleted')
        : 'modified';
      // Send change event to the renderer that requested the watch
      try {
        e.sender.send('fs:change', { kind, path: fullPath });
      } catch (_) {
        // Window may have been closed
      }
    });
    watchers.set(watchId, watcher);
    return watchId;
  } catch (_) {
    return null;
  }
});

ipcMain.handle('fs:unwatch', (_e, watchId) => {
  const watcher = watchers.get(watchId);
  if (watcher) {
    watcher.close();
    watchers.delete(watchId);
  }
});

