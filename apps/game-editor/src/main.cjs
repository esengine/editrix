ipcMain.on('open-project', (_e, projectPath) => {
  currentProjectPath = projectPath || null;

  // Auto-update plugin type declarations on every project open
  if (projectPath) {
    generatePluginTypes(projectPath);
  }

  // Update last opened time
  if (projectPath) {
    const config = loadLauncherConfig();
    const entry = config.projects.find((p) => p.path === projectPath);
    if (entry) {
      entry.lastOpened = new Date().toISOString();
      saveLauncherConfig(config);
    }
  }

  if (launcherWindow) {
    launcherWindow.close();
    launcherWindow = null;
  }
  if (!mainWindow) {
    createEditorWindow();
  }
});

