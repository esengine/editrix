// ── Launcher Config (project list persistence) ──────────

function ensureDir(dir) {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
}

function loadLauncherConfig() {
  try {
    if (fs.existsSync(LAUNCHER_CONFIG_PATH)) {
      return JSON.parse(fs.readFileSync(LAUNCHER_CONFIG_PATH, 'utf-8'));
    }
  } catch (_) {
    // Corrupted config — start fresh
  }
  return { projects: [] };
}

function saveLauncherConfig(config) {
  ensureDir(EDITRIX_HOME);
  fs.writeFileSync(LAUNCHER_CONFIG_PATH, JSON.stringify(config, null, 2), 'utf-8');
}

