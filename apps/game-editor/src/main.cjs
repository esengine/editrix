function createProjectOnDisk(projectPath, projectConfig) {
  ensureDir(projectPath);
  ensureDir(path.join(projectPath, '.editrix'));
  ensureDir(path.join(projectPath, 'assets'));
  ensureDir(path.join(projectPath, 'scenes'));

  // Write editrix.json
  fs.writeFileSync(
    path.join(projectPath, 'editrix.json'),
    JSON.stringify(projectConfig, null, 2),
    'utf-8',
  );

  // Write .editrix/settings.json (empty user overrides)
  fs.writeFileSync(
    path.join(projectPath, '.editrix', 'settings.json'),
    JSON.stringify({}, null, 2),
    'utf-8',
  );

  // Write .gitignore
  fs.writeFileSync(
    path.join(projectPath, '.gitignore'),
    [
      '.editrix/settings.json',
      '.editrix/cache/',
      '.editrix/types/',
      'node_modules/',
      '',
    ].join('\n'),
    'utf-8',
  );

  // Generate plugin type declarations
  generatePluginTypes(projectPath);

  // Write a default scene file with proper format
  fs.writeFileSync(
    path.join(projectPath, 'scenes', 'main.scene.json'),
    JSON.stringify({
      $type: 'editrix:scene',
      $version: 1,
      name: 'Main Scene',
      nodeTypes: [
        {
          type: 'gameobject',
          label: 'Game Object',
          properties: [
            { key: 'position.x', label: 'Position X', type: 'number', defaultValue: 0, group: 'Transform' },
            { key: 'position.y', label: 'Position Y', type: 'number', defaultValue: 0, group: 'Transform' },
            { key: 'position.z', label: 'Position Z', type: 'number', defaultValue: 0, group: 'Transform' },
            { key: 'rotation.x', label: 'Rotation X', type: 'range', defaultValue: 0, min: 0, max: 360, step: 1, group: 'Transform' },
            { key: 'rotation.y', label: 'Rotation Y', type: 'range', defaultValue: 0, min: 0, max: 360, step: 1, group: 'Transform' },
            { key: 'rotation.z', label: 'Rotation Z', type: 'range', defaultValue: 0, min: 0, max: 360, step: 1, group: 'Transform' },
            { key: 'active', label: 'Active', type: 'boolean', defaultValue: true, group: 'General' },
          ],
        },
      ],
      nodes: [
        { id: 'root', name: 'Root', type: 'gameobject', icon: 'layers', visible: true, properties: {} },
      ],
    }, null, 2),
    'utf-8',
  );

  // Add to launcher config
  const config = loadLauncherConfig();
  // Remove existing entry for same path if any
  config.projects = config.projects.filter((p) => p.path !== projectPath);
  config.projects.unshift({
    path: projectPath,
    name: projectConfig.name,
    editrixVersion: projectConfig.editrix,
    lastOpened: new Date().toISOString(),
    starred: false,
  });
  saveLauncherConfig(config);
}

