ipcMain.on('window-minimize', (e) => {
  BrowserWindow.fromWebContents(e.sender)?.minimize();
});

ipcMain.on('window-maximize', (e) => {
  const win = BrowserWindow.fromWebContents(e.sender);
  if (win?.isMaximized()) {
    win.unmaximize();
  } else {
    win?.maximize();
  }
});

ipcMain.on('window-close', (e) => {
  BrowserWindow.fromWebContents(e.sender)?.close();
});

// ── IPC: System ─────────────────────────────────────────

ipcMain.on('get-home-path', (e) => {
  e.returnValue = os.homedir();
});

ipcMain.handle('select-folder', async (e) => {
  const win = BrowserWindow.fromWebContents(e.sender);
  const result = await dialog.showOpenDialog(win, {
    properties: ['openDirectory'],
    title: 'Select Project Location',
  });
  if (result.canceled || result.filePaths.length === 0) return null;
  return result.filePaths[0];
});

// ── IPC: Project Management ─────────────────────────────

ipcMain.handle('list-projects', () => {
  return loadLauncherConfig().projects;
});

ipcMain.handle('create-project', (_e, { projectPath, projectConfig }) => {
  try {
    createProjectOnDisk(projectPath, projectConfig);
    return { success: true };
  } catch (err) {
    return { success: false, error: String(err) };
  }
});

ipcMain.handle('toggle-star', (_e, projectPath) => {
  const config = loadLauncherConfig();
  const entry = config.projects.find((p) => p.path === projectPath);
  if (entry) {
    entry.starred = !entry.starred;
    saveLauncherConfig(config);
    return entry.starred;
  }
  return false;
});

ipcMain.handle('update-last-opened', (_e, projectPath) => {
  const config = loadLauncherConfig();
  const entry = config.projects.find((p) => p.path === projectPath);
  if (entry) {
    entry.lastOpened = new Date().toISOString();
    saveLauncherConfig(config);
  }
});

